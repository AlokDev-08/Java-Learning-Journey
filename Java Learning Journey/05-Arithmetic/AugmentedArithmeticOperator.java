public class AugmentedArithmeticOperator {
    public static void main(String[] args) {

//        Augmented Arithmetic Operators

        int x = 10;
        int y = 20;

        x +=y;  // x = x + y
        x -=y;  // x = x - y
        x *=y;  // x = x * y
        x /=y;  // x = x / y
        x %=y;  // x = x % y

        System.out.println(x);

    }
}
